            THEN SUM(CASE WHEN R.NOMBRE_RECURSO = 'Staff' THEN UP.HH_STAFF END)
            ELSE MAX(CASE WHEN R.NOMBRE_RECURSO = 'Staff' THEN UP.HH_STAFF END)
