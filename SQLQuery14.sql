ALTER PROCEDURE ObtenerProyectosPorUsuario
    @IDUsuario INT
AS
BEGIN
    SET NOCOUNT ON;

    SELECT 
        up.ID_PROYECTO,
        p.Nombre AS Nombre_Proyecto,  -- Suponiendo que hay una columna Nombre en la tabla PROYECTO
        up.ID_USUARIO,
        P.NUM_PROYECTO,
		C.NOMBRE AS NOMBRECLIENTE   -- Suponiendo que hay una columna Nombre en la tabla USUARIO
        
    FROM 
        USUARIO_PROYECTO up
    INNER JOIN 
        PROYECTO p ON up.ID_PROYECTO = p.ID
    INNER JOIN 
        USUARIO u ON up.ID_USUARIO = u.ID
	INNER JOIN SUCURSAL_CLIENTE SC ON SC.ID = P.ID_CLIENTE_SUCURSAL
	INNER JOIN CLIENTE C ON C.ID = SC.ID_CLIENTE
    WHERE 
        p.STATUS_PROYECTO = 2  -- Proyectos en estado 2
        AND up.ID_USUARIO = @IDUsuario  -- Filtrar por ID_USUARIO
        AND (
            up.HH_SOCIOS > 0 OR   -- Condición para horas de socios
            up.HH_STAFF > 0 OR    -- Condición para horas de staff
            up.HH_CONSULTORA > 0 OR  -- Condición para horas de Consultor A
            up.HH_CONSULTORB > 0 OR   -- Condición para horas de Consultor B
            up.HH_CONSULTORC > 0     -- Condición para horas de Consultor C
        )
    ORDER BY 
        up.ID_PROYECTO;  -- Ordenar los resultados por proyecto
END




