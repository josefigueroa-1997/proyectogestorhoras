CREATE PROCEDURE [dbo].[RECUPERARPLANILLAUSUARIO_EXCEL]
@IDPLANILLA INT
AS
BEGIN
    SELECT P.ID AS IDPLANILLA, 
           p.MES, 
           P.ANIO, 
           PU.FECHA_REGISTRO, 
           PRO.NOMBRE AS NOMBREPROYECTO, 
           PRO.NUM_PROYECTO AS NUMPROYECTO, 
           PRO.ID AS IDPROYECTO, 
           A.NOMBRE AS NOMBREACTIVIDAD, 
           PU.REGISTRO_HH_PROYECTO AS HHREGISTRADAS,
           PU.OBSERVACIONES
    FROM PLANILLA P
    INNER JOIN PLANILLA_USUSARIO_PROYECTO PU ON PU.ID_PLANILLA = P.ID
    INNER JOIN USUARIO_PROYECTO UP ON PU.ID_USU_PROY = UP.ID
    INNER JOIN PROYECTO PRO ON PRO.ID = UP.ID_PROYECTO
    INNER JOIN ACTIVIDADES A ON PU.ID_ACTIVIDAD = A.ID
    WHERE 
      P.ID = @IDPLANILLA 
    ORDER BY PU.FECHA_REGISTRO 
END;
